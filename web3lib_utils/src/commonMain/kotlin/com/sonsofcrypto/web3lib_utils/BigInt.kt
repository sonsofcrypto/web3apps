package com.sonsofcrypto.web3lib_utils

class BigInt {

    fun mul(value: BigInt): BigInt {
        return this
    }

    fun add(value: BigInt): BigInt {
        return this
    }

    companion object {

        fun from(string: String): BigInt {
            return BigInt()
        }

        fun from(int: Int): BigInt {
            return BigInt()
        }
    }
}